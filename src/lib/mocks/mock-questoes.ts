import type { Alternativa, Questao } from "@/types";

// Helper para construir alternativas com 4 opções (A, B, C, D)
function alternativas(
  questaoId: string,
  textos: [string, string, string, string],
  indiceCorreta: 0 | 1 | 2 | 3,
): Alternativa[] {
  return textos.map(
    (texto, ordem): Alternativa => ({
      id: `alt_${questaoId}_${String.fromCharCode(97 + ordem)}`,
      texto,
      correta: ordem === indiceCorreta,
      ordem,
    }),
  );
}

